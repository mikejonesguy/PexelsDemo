package com.mike.pexelsdemo.model

data class PhotoSrc(
    val original: String?,
    val large2x: String?,
    val large: String?,
    val medium: String?,
    val small: String?,
    val portrait: String?,
    val landscape: String?,
    val tiny: String?,
) {
    fun getBestFullUrl(): String? {
        return original ?: large2x ?: large ?: medium
    }

    fun getBestThumbUrl(): String? {
        return medium ?: small ?: tiny
    }
}

